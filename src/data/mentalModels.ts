// Helper functions
export const getModelByCode = (code: string): MentalModel | undefined => {
  return mentalModels.find(m => m.code === code);
};

export const getModelsByTransformation = (transformation: TransformationType): MentalModel[] => {
  return mentalModels.filter(m => m.transformation === transformation);
};

export const getModelsByDifficulty = (difficulty: MentalModel['difficulty']): MentalModel[] => {
  return mentalModels.filter(m => m.difficulty === difficulty);
};

export const searchModels = (query: string): MentalModel[] => {
  const lowerQuery = query.toLowerCase();
  return mentalModels.filter(m =>
    m.name.toLowerCase().includes(lowerQuery) ||
    m.description.toLowerCase().includes(lowerQuery) ||
    m.tags.some(tag => tag.toLowerCase().includes(lowerQuery))
  );
};

export const getRelatedModels = (code: string): MentalModel[] => {
  const model = getModelByCode(code);
  if (!model) return [];
  
  return model.relatedModels
    .map(relatedCode => getModelByCode(relatedCode))
    .filter((m): m is MentalModel => m !== undefined);
};
